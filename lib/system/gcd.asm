GCD:
  PUSH(FP)
  MOV(FP, SP)
  PUSH(R1)
  PUSH(R2)
  PUSH(R3)
  MOV(R1, FPARG(0))
  MOV(R2, FPARG(1))
GCD_LOOP:
  CMP(R2, IMM(0))
  JUMP_EQ(GCD_LOOP_END)
  MOV(R3, R1)
  MOV(R1, R2)
  REM(R3, R2)
  MOV(R2, R3)
  JUMP(GCD_LOOP)
GCD_LOOP_END:
  MOV(R0, R1)
  POP(R3)
  POP(R2)
  POP(R1)
  POP(FP)
  RETURN
